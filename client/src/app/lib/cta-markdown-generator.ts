/**
 * CTA Markdown Generator
 * =======================
 * 为结果页"下一步动作"CTA 按钮生成丰富的 Markdown 内容，
 * 用于在 CozeEditorDrawer 中展示。
 *
 * 根据 CTA 的 id 和 result 上下文生成对应的报告/脚本/策略内容。
 */

import type { ResultRecord } from "../store/app-data";

export function generateCtaMarkdown(
  ctaId: string,
  ctaTitle: string,
  ctaPrompt: string,
  result: ResultRecord,
): string {
  const now = new Date();
  const dateStr = `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, "0")}-${String(now.getDate()).padStart(2, "0")} ${String(now.getHours()).padStart(2, "0")}:${String(now.getMinutes()).padStart(2, "0")}`;

  // 低粉爆款拆解相关 CTA
  if (ctaId === "rewrite_script") {
    return generateRewriteScript(result, dateStr);
  }
  if (ctaId === "extract_copy") {
    return generateExtractCopy(result, dateStr);
  }
  if (ctaId === "topic_strategy") {
    return generateTopicStrategy(result, dateStr);
  }

  // 爆款拆解相关 CTA
  if (ctaId === "remake_script") {
    return generateRemakeScript(result, dateStr);
  }
  if (ctaId === "extract_hooks") {
    return generateExtractHooks(result, dateStr);
  }
  if (ctaId === "find_similar") {
    return generateFindSimilar(result, dateStr);
  }

  // 账号诊断相关 CTA
  if (ctaId === "account_playbook") {
    return generateAccountPlaybook(result, dateStr);
  }
  if (ctaId === "benchmark_accounts") {
    return generateBenchmarkAccounts(result, dateStr);
  }
  if (ctaId === "stop_list") {
    return generateStopList(result, dateStr);
  }

  // 选题策略相关 CTA
  if (ctaId === "weekly_plan") {
    return generateWeeklyPlan(result, dateStr);
  }
  if (ctaId === "topic_scripts") {
    return generateTopicScripts(result, dateStr);
  }
  if (ctaId === "priority_validate") {
    return generatePriorityValidate(result, dateStr);
  }

  // 文案提取相关 CTA
  if (ctaId === "rewrite_pack") {
    return generateRewritePack(result, dateStr);
  }
  if (ctaId === "hook_library") {
    return generateHookLibrary(result, dateStr);
  }
  if (ctaId === "cta_patterns") {
    return generateCtaPatterns(result, dateStr);
  }

  // 通用 fallback
  return generateGenericCta(ctaTitle, ctaPrompt, result, dateStr);
}

